#!/usr/bin/env bash
# fix_venv.sh - Rebuild .venv/bin/activate and add an auto-activation hook
# to ~/.bashrc so you never have to type `source .venv/bin/activate` again.
#
# Idempotent and non-destructive: only touches the activate file and appends
# a marked block to ~/.bashrc (no overwrites).
#
# Run once:
#   bash scripts/fix_venv.sh
# Then open a new SSH session (or `source ~/.bashrc`) and the venv activates
# automatically when you cd into the repo or login.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" >/dev/null 2>&1 && pwd -P)"
REPO_DIR="$(cd "$SCRIPT_DIR/.." >/dev/null 2>&1 && pwd -P)"
VENV_DIR="$REPO_DIR/.venv"
ACTIVATE_FILE="$VENV_DIR/bin/activate"

log()  { printf "\033[1;34m[fix-venv]\033[0m %s\n" "$*"; }
warn() { printf "\033[1;33m[fix-venv-warn]\033[0m %s\n" "$*" >&2; }
die()  { printf "\033[1;31m[fix-venv-error]\033[0m %s\n" "$*" >&2; exit 1; }

[ -d "$VENV_DIR" ] || die "No .venv found at $VENV_DIR. Run setup_lambdavector2.sh first."
[ -x "$VENV_DIR/bin/python" ] || die "No python in $VENV_DIR/bin. The venv is broken; recreate it."

log "rewriting $ACTIVATE_FILE"
cat > "$ACTIVATE_FILE" <<'ACTIVATE_EOF'
# Generated by scripts/fix_venv.sh
deactivate () {
    if [ -n "${_OLD_VIRTUAL_PATH:-}" ] ; then
        PATH="${_OLD_VIRTUAL_PATH:-}"
        export PATH
        unset _OLD_VIRTUAL_PATH
    fi
    if [ -n "${_OLD_VIRTUAL_PS1:-}" ] ; then
        PS1="${_OLD_VIRTUAL_PS1:-}"
        export PS1
        unset _OLD_VIRTUAL_PS1
    fi
    unset VIRTUAL_ENV
    unset VIRTUAL_ENV_PROMPT
    if [ ! "${1:-}" = "nondestructive" ] ; then
        unset -f deactivate
    fi
}
deactivate nondestructive
VIRTUAL_ENV="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
export VIRTUAL_ENV
_OLD_VIRTUAL_PATH="$PATH"
PATH="$VIRTUAL_ENV/bin:$PATH"
export PATH
_OLD_VIRTUAL_PS1="${PS1:-}"
PS1="(scaleshift-bench) ${PS1:-}"
export PS1
VIRTUAL_ENV_PROMPT="(scaleshift-bench) "
export VIRTUAL_ENV_PROMPT
hash -r 2>/dev/null || true
ACTIVATE_EOF

log "activate rebuilt OK"

# Sanity: source it in a sub-shell, check that python resolves into the venv.
bash -c "source '$ACTIVATE_FILE' && command -v python | grep -q '$VENV_DIR/bin/python'" \
    || die "post-rebuild sanity check failed"
log "sanity check: 'python' inside the venv resolves correctly"

HOOK_MARKER="# >>> scaleshift-bench auto-activate >>>"
HOOK_END="# <<< scaleshift-bench auto-activate <<<"
BASHRC="$HOME/.bashrc"

if grep -qF "$HOOK_MARKER" "$BASHRC" 2>/dev/null; then
    log "auto-activate hook already present in $BASHRC; not duplicating"
else
    log "appending auto-activate hook to $BASHRC"
    {
        printf "\n%s\n" "$HOOK_MARKER"
        printf "# Auto-activate scaleshift-bench venv on shell startup.\n"
        printf "if [ -z \"\${VIRTUAL_ENV:-}\" ] && [ -f \"%s\" ]; then\n" "$ACTIVATE_FILE"
        printf "    source \"%s\"\n" "$ACTIVATE_FILE"
        printf "fi\n"
        printf "# Default to GPU 1 on lambdavector2 (GPU 0 is shared / often busy).\n"
        printf "export CUDA_VISIBLE_DEVICES=1\n"
        printf "%s\n" "$HOOK_END"
    } >> "$BASHRC"
    log "hook added"
fi

log ""
log "Done."
log "For THIS session, run:"
log "    source $ACTIVATE_FILE"
log ""
log "Future SSH sessions will auto-activate. Test with:"
log "    bash -lc 'echo \$VIRTUAL_ENV'"
log "(should print a path ending in /scaleshift-bench/.venv)"
